let currentLang = 'en';
let currentPage = 1;
let currentDisplayItems = [];
let currentTab = 'shaders';
let allShadersElements = [];
let disclaimerExpanded = false;
const ITEMS_PER_PAGE = 5;

function buildShaderElements() {
    const container = document.getElementById('downloadList');
    container.innerHTML = '';
    allShadersElements = [];
    shadersData.forEach((shader, index) => {
        const item = document.createElement('a');
        item.href = shader.url;
        item.className = 'download-item';
        item.target = '_blank';
        item.style.animationDelay = `${0.05 + (index * 0.05)}s`;
        item.innerHTML = `
            <div class="item-icon"><span class="material-symbols-outlined">description</span></div>
            <div class="item-content">
                <div class="item-title">${shader.title}</div>
                <div class="badge-group">
                    <div class="badge-row"><span class="meta-badge">Developer: ${shader.developer}</span></div>
                    <div class="badge-row"><span class="meta-badge-new">Edited by ${shader.editor}</span></div>
                </div>
            </div>
            <span class="material-symbols-outlined arrow-icon">chevron_right</span>
        `;
        container.appendChild(item);
        allShadersElements.push(item);
    });
    currentDisplayItems = [...allShadersElements];
}

function renderPage() {
    const total = Math.ceil(currentDisplayItems.length / ITEMS_PER_PAGE);
    let page = currentPage;
    if (page < 1) page = 1;
    if (page > total && total > 0) page = total;
    if (total === 0) page = 1;
    currentPage = page;
    
    allShadersElements.forEach(el => el.style.display = 'none');
    const start = (currentPage - 1) * ITEMS_PER_PAGE;
    const end = start + ITEMS_PER_PAGE;
    for (let i = start; i < end && i < currentDisplayItems.length; i++) {
        currentDisplayItems[i].style.display = 'flex';
    }
    
    const t = translations[currentLang];
    const prefix = t?.pagePrefix || 'Page';
    document.getElementById('pageIndicatorTop').innerText = `${prefix} ${currentPage}`;
    document.getElementById('pageIndicatorBottom').innerText = `${prefix} ${currentPage}`;
    
    const prevBtns = [document.getElementById('prevBtnTop'), document.getElementById('prevBtnBottom')];
    const nextBtns = [document.getElementById('nextBtnTop'), document.getElementById('nextBtnBottom')];
    prevBtns.forEach(btn => {
        if (currentPage === 1) btn.classList.add('disabled');
        else btn.classList.remove('disabled');
    });
    nextBtns.forEach(btn => {
        if (currentPage === total || total === 0) btn.classList.add('disabled');
        else btn.classList.remove('disabled');
    });
    
    const info = document.getElementById('paginationInfo');
    if (info && t) {
        if (currentDisplayItems.length === 0) info.innerText = t.noResultText;
        else info.innerText = `${t.showingText} ${start+1} - ${Math.min(end, currentDisplayItems.length)} ${t.ofText} ${currentDisplayItems.length} ${t.shadersText}`;
    }
}

function updateDisclaimerReadButton() {
    const btn = document.getElementById('disclaimerReadBtn');
    const textDiv = document.getElementById('disclaimerText');
    const t = translations[currentLang];
    if (btn && t) {
        btn.querySelector('span:first-child').innerText = disclaimerExpanded ? t.closeBtn : t.readBtn;
        if (disclaimerExpanded) {
            btn.classList.add('expanded');
            textDiv.classList.add('expanded');
        } else {
            btn.classList.remove('expanded');
            textDiv.classList.remove('expanded');
        }
    }
}

function toggleDisclaimer() {
    disclaimerExpanded = !disclaimerExpanded;
    updateDisclaimerReadButton();
}

function applyLanguage(lang) {
    const t = translations[lang];
    if (!t) return;
    
    currentLang = lang;
    
    document.getElementById('heroTitle').innerText = t.heroTitle;
    document.getElementById('heroSubhead').innerText = t.heroSubhead;
    document.getElementById('followLabel').innerText = t.followLabel;
    document.getElementById('donateTitle').innerHTML = t.donateTitle;
    document.getElementById('donateSub').innerText = t.donateSub;
    document.getElementById('disclaimerTitle').innerText = t.disclaimerTitle || 'Disclaimer';
    document.getElementById('disclaimerText').innerHTML = t.disclaimerText;
    document.getElementById('searchInput').placeholder = t.searchPlaceholder;
    document.getElementById('comingSoonTitle').innerText = t.comingSoonTitle;
    document.getElementById('comingSoonDesc').innerText = t.comingSoonDesc;
    
    document.querySelectorAll('#prevText, #prevTextBottom').forEach(el => el.innerText = t.prevText);
    document.querySelectorAll('#nextText, #nextTextBottom').forEach(el => el.innerText = t.nextText);
    document.getElementById('currentLangLabel').innerText = t.langName || lang.toUpperCase();
    
    disclaimerExpanded = false;
    updateDisclaimerReadButton();
    
    renderPage();
    localStorage.setItem('seiyantLang', lang);
}

function switchTab(tab) {
    currentTab = tab;
    const shadersTab = document.getElementById('shadersTabBtn');
    const modpacksTab = document.getElementById('modpacksTabBtn');
    const downloadList = document.getElementById('downloadList');
    const comingSoon = document.getElementById('comingSoonArea');
    const mainContent = document.querySelector('.main-content');
    
    if (tab === 'shaders') {
        shadersTab.classList.add('active');
        modpacksTab.classList.remove('active');
        downloadList.style.display = 'flex';
        comingSoon.style.display = 'none';
        mainContent.classList.remove('modpacks-active');
        currentDisplayItems = [...allShadersElements];
        currentPage = 1;
        renderPage();
        document.getElementById('searchInput').value = '';
    } else {
        modpacksTab.classList.add('active');
        shadersTab.classList.remove('active');
        downloadList.style.display = 'none';
        comingSoon.style.display = 'block';
        mainContent.classList.add('modpacks-active');
    }
}

// Search
document.getElementById('searchInput').addEventListener('input', (e) => {
    if (currentTab !== 'shaders') return;
    const keyword = e.target.value.toLowerCase();
    currentDisplayItems = allShadersElements.filter(item => 
        item.querySelector('.item-title').innerText.toLowerCase().includes(keyword)
    );
    currentPage = 1;
    renderPage();
});

// Pagination
function setupPagination() {
    const prevTop = document.getElementById('prevBtnTop');
    const nextTop = document.getElementById('nextBtnTop');
    const prevBottom = document.getElementById('prevBtnBottom');
    const nextBottom = document.getElementById('nextBtnBottom');
    
    if (prevTop) prevTop.addEventListener('click', () => { if (currentPage > 1 && currentTab === 'shaders') { currentPage--; renderPage(); } });
    if (nextTop) nextTop.addEventListener('click', () => { const total = Math.ceil(currentDisplayItems.length / ITEMS_PER_PAGE); if (currentPage < total && currentTab === 'shaders') { currentPage++; renderPage(); } });
    if (prevBottom) prevBottom.addEventListener('click', () => { if (currentPage > 1 && currentTab === 'shaders') { currentPage--; renderPage(); } });
    if (nextBottom) nextBottom.addEventListener('click', () => { const total = Math.ceil(currentDisplayItems.length / ITEMS_PER_PAGE); if (currentPage < total && currentTab === 'shaders') { currentPage++; renderPage(); } });
}

// Theme Toggle
let currentTheme = 'light';
function applyTheme(theme) {
    if (theme === 'dark') {
        document.body.classList.add('dark');
        const themeIcon = document.querySelector('#themeToggleBtn .material-symbols-outlined');
        if (themeIcon) themeIcon.innerText = 'light_mode';
    } else {
        document.body.classList.remove('dark');
        const themeIcon = document.querySelector('#themeToggleBtn .material-symbols-outlined');
        if (themeIcon) themeIcon.innerText = 'dark_mode';
    }
    currentTheme = theme;
    localStorage.setItem('seiyantTheme', theme);
}

function toggleTheme() {
    applyTheme(currentTheme === 'light' ? 'dark' : 'light');
}

// Language Popup handlers
function initLangPopup() {
    const langWrapper = document.getElementById('langPopupWrapper');
    const langBtn = document.getElementById('langBtn');
    
    document.addEventListener('click', (e) => {
        if (!langWrapper.contains(e.target)) langWrapper.classList.remove('active');
    });
    
    langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langWrapper.classList.toggle('active');
    });
    
    document.querySelectorAll('[data-lang]').forEach(opt => {
        opt.addEventListener('click', () => {
            applyLanguage(opt.dataset.lang);
            langWrapper.classList.remove('active');
        });
    });
}

// Tab buttons
document.getElementById('shadersTabBtn')?.addEventListener('click', () => switchTab('shaders'));
document.getElementById('modpacksTabBtn')?.addEventListener('click', () => switchTab('modpacks'));

// Theme toggle button
document.getElementById('themeToggleBtn')?.addEventListener('click', toggleTheme);

// Disclaimer Read More button
document.getElementById('disclaimerReadBtn')?.addEventListener('click', toggleDisclaimer);

// Initialize
buildShaderElements();
setupPagination();
initLangPopup();

// Load saved theme
const savedTheme = localStorage.getItem('seiyantTheme');
if (savedTheme) applyTheme(savedTheme);
else applyTheme('light');

// Load saved language
const savedLang = localStorage.getItem('seiyantLang');
if (savedLang && translations[savedLang]) applyLanguage(savedLang);
else applyLanguage('en');

switchTab('shaders');