const ITEMS_PER_PAGE = 5;

function buildShaderElements() {
    const container = document.getElementById('downloadList');
    container.innerHTML = '';
    allShadersElements = [];
    shadersData.forEach((shader, index) => {
        const item = document.createElement('a');
        item.href = shader.url;
        item.className = 'download-item';
        item.target = '_blank';
        item.style.animationDelay = `${0.05 + (index * 0.05)}s`;
        item.innerHTML = `
            <div class="item-icon"><span class="material-symbols-outlined">description</span></div>
            <div class="item-content">
                <div class="item-title">${shader.title}</div>
                <div class="badge-group">
                    <div class="badge-row"><span class="meta-badge">Developer: ${shader.developer}</span></div>
                    <div class="badge-row"><span class="meta-badge-new">Edited by ${shader.editor}</span></div>
                </div>
            </div>
            <span class="material-symbols-outlined arrow-icon">chevron_right</span>
        `;
        container.appendChild(item);
        allShadersElements.push(item);
    });
    currentDisplayItems = [...allShadersElements];
}

function buildModpackElements() {
    const container = document.getElementById('modpackList');
    container.innerHTML = '';
    const t = translations[currentLang];
    
    modpacksData.forEach((modpack, index) => {
        const item = document.createElement('a');
        item.href = modpack.url;
        item.className = 'download-item modpack-item';
        item.target = '_blank';
        item.style.animationDelay = `${0.05 + (index * 0.05)}s`;
        
        const title = t[modpack.titleKey] || modpack.titleKey;
        const subtitle = t[modpack.subtitleKey] || modpack.subtitleKey;
        
        // Setiap badge dibuat terpisah di badge-row sendiri
        let badgesHTML = modpack.badges.map((badgeKey) => {
            const badgeText = t[badgeKey] || badgeKey;
            // Badge "by Seiyant" menggunakan meta-badge-new (sama seperti shaders)
            if (badgeKey === "badgeBy") {
                return `<div class="badge-row"><span class="meta-badge-new">${badgeText}</span></div>`;
            }
            return `<div class="badge-row"><span class="meta-badge">${badgeText}</span></div>`;
        }).join('');
        
        item.innerHTML = `
            <div class="item-icon"><span class="material-symbols-outlined">package_2</span></div>
            <div class="item-content">
                <div class="item-title modpack-title">${title}</div>
                <div class="item-subtitle">${subtitle}</div>
                <div class="badge-group">
                    ${badgesHTML}
                </div>
            </div>
            <span class="material-symbols-outlined arrow-icon">chevron_right</span>
        `;
        container.appendChild(item);
    });
}

function renderPage() {
    const total = Math.ceil(currentDisplayItems.length / ITEMS_PER_PAGE);
    let page = currentPage;
    if (page < 1) page = 1;
    if (page > total && total > 0) page = total;
    if (total === 0) page = 1;
    currentPage = page;
    
    allShadersElements.forEach(el => el.style.display = 'none');
    const start = (currentPage - 1) * ITEMS_PER_PAGE;
    const end = start + ITEMS_PER_PAGE;
    for (let i = start; i < end && i < currentDisplayItems.length; i++) {
        currentDisplayItems[i].style.display = 'flex';
    }
    
    const t = translations[currentLang];
    const prefix = t?.pagePrefix || 'Page';
    document.getElementById('pageIndicatorTop').innerText = `${prefix} ${currentPage}`;
    document.getElementById('pageIndicatorBottom').innerText = `${prefix} ${currentPage}`;
    
    const prevBtns = [document.getElementById('prevBtnTop'), document.getElementById('prevBtnBottom')];
    const nextBtns = [document.getElementById('nextBtnTop'), document.getElementById('nextBtnBottom')];
    prevBtns.forEach(btn => {
        if (currentPage === 1) btn.classList.add('disabled');
        else btn.classList.remove('disabled');
    });
    nextBtns.forEach(btn => {
        if (currentPage === total || total === 0) btn.classList.add('disabled');
        else btn.classList.remove('disabled');
    });
    
    const info = document.getElementById('paginationInfo');
    if (info && t) {
        if (currentDisplayItems.length === 0) info.innerText = t.noResultText;
        else info.innerText = `${t.showingText} ${start+1} - ${Math.min(end, currentDisplayItems.length)} ${t.ofText} ${currentDisplayItems.length} ${t.shadersText}`;
    }
}

function updateDisclaimerReadButton() {
    const btn = document.getElementById('disclaimerReadBtn');
    const textDiv = document.getElementById('disclaimerText');
    const t = translations[currentLang];
    if (btn && t) {
        btn.querySelector('span:first-child').innerText = disclaimerExpanded ? t.closeBtn : t.readBtn;
        if (disclaimerExpanded) {
            btn.classList.add('expanded');
            textDiv.classList.add('expanded');
        } else {
            btn.classList.remove('expanded');
            textDiv.classList.remove('expanded');
        }
    }
}

function toggleDisclaimer() {
    disclaimerExpanded = !disclaimerExpanded;
    updateDisclaimerReadButton();
}

function applyLanguage(lang) {
    const t = translations[lang];
    if (!t) return;
    
    currentLang = lang;
    
    // Update hero based on current tab
    updateHeroText();
    
    document.getElementById('followLabel').innerText = t.followLabel;
    document.getElementById('donateTitle').innerHTML = t.donateTitle;
    document.getElementById('donateSub').innerText = t.donateSub;
    document.getElementById('disclaimerTitle').innerText = t.disclaimerTitle || 'Disclaimer';
    document.getElementById('disclaimerText').innerHTML = t.disclaimerText;
    document.getElementById('searchInput').placeholder = t.searchPlaceholder;
    document.getElementById('comingSoonTitle').innerText = t.comingSoonTitle;
    document.getElementById('comingSoonDesc').innerText = t.comingSoonDesc;
    document.getElementById('modpackDisclaimerTitle').innerText = t.modpackDisclaimerTitle || 'Disclaimer';
    document.getElementById('modpackDisclaimerText').innerHTML = t.modpackDisclaimerText || '';
    
    document.querySelectorAll('#prevText, #prevTextBottom').forEach(el => el.innerText = t.prevText);
    document.querySelectorAll('#nextText, #nextTextBottom').forEach(el => el.innerText = t.nextText);
    document.getElementById('currentLangLabel').innerText = t.langName || lang.toUpperCase();
    
    disclaimerExpanded = false;
    updateDisclaimerReadButton();
    
    // Rebuild modpack elements with new language
    buildModpackElements();
    
    renderPage();
    localStorage.setItem('seiyantLang', lang);
}

function updateHeroText() {
    const t = translations[currentLang];
    if (!t) return;
    
    const heroTitle = document.getElementById('heroTitle');
    const heroSubhead = document.getElementById('heroSubhead');
    
    if (currentTab === 'shaders') {
        heroTitle.innerText = t.heroTitle;
        heroSubhead.innerText = t.heroSubhead;
        document.querySelector('.main-content').classList.remove('modpacks-active');
    } else {
        heroTitle.innerText = t.modpackHeroTitle;
        heroSubhead.innerText = t.modpackHeroSubhead;
        document.querySelector('.main-content').classList.add('modpacks-active');
    }
}

